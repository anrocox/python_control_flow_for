#the for loop iterate over the items of any sequence such as a list or a string

#create a string
s = 'Hello'

#iterates over the items of any sequence 's' in the order in which they appear
#in the sequence.
for i in s:

    #print i, where in each iteration is a letter of the word 'Hello'
    print(i)
